def create_mob(wave):
    """
    wave에 따라 몬스터 리스트 반환.
    """
    mob = []
    for _ in range(wave//3+5):
        mob.append({
        'x': 50,
        'y': 40,
        'size': 1,
        'speed': 4,
        'tmp_speed': 0,
        'hp': wave//2+1,
        'direction': 0,
        'color': (random.randint(0,255), random.randint(0,255), random.randint(0,255))
        })
    return mob

