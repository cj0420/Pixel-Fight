def move_fireballs():
    rl = []
    for f in fireballs:
        f['tmp_speed'] += 1
        if f['tmp_speed'] >= f['speed']:
            f['tmp_speed'] = 0
        if f['direction']==1:
            f['y']-=1
        elif f['direction']==2:
            f['y']+=1
        elif f['direction']==3:
            f['x']-=1
        elif f['direction']==4:
            f['x']+=1  
        if wall[f['y']][f['x']] == 1:
            rl.append(f)
    
    for re in rl:
        fireballs.remove(re)


